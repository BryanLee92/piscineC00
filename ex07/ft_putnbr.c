#include <unistd.h>

void	ft_putnbr(int nb)
{
	int	a;
	int	b;
	int	rmn;
	int	num;

	a = nb / 10;
	b = nb % 10;
	num = a + 48;
	rmn = b + 48;
	write (1, &num, 1);
	write (1, &rmn, 1);
}
